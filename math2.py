# complex() can be used when working with complex functions
z = complex(2,-3)
print(z)
print(z.real)
print(z.imag)

# manipulating strings
# using a + to join strings = concatenation
first_name = "Alan"
middle_init = "C"
last_name = "Simpson"
# quotes with space will add spaces since python doesn't auto add
full_name = first_name + " " + middle_init + ". " + last_name
print(full_name)

# to find the length of a string
s1 = ""
s2 = " "
s3 = " A B C"
print(len(s1))
print(len(s2))
print(len(s3))